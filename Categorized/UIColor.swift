import Foundation
import UIKit

extension UIColor{
    
    class func MPCharcoalColor() -> UIColor {
        return UIColor(red:0.16, green:0.16, blue:0.17, alpha:1)
    }
    
    class func MPlightSilverColor() -> UIColor {
        return UIColor(red:0.95, green:0.95, blue:0.95, alpha:1)
    }
    
    class func FTGoldenColor() -> UIColor {
        return UIColor(red:0.79, green:0.64, blue:0.36, alpha:1)
    }
    
    class func FTDarkGrayColor() -> UIColor {
        return UIColor(red:55/250, green:55/250, blue:58/250, alpha:1)
    }
    
    class func AppleBlue() -> UIColor {
        return UIColor(red: 0.0, green: 122.0/255.0, blue: 1.0, alpha: 1.0)
    }
    
    class func notePadYellow() -> UIColor {
        return UIColor(red:1, green:0.84, blue:0.19, alpha:1)
    }
    
    class func darkYellow() -> UIColor {
        return UIColor(red:0.89, green:0.69, blue:0.06, alpha:1)
    }
    
    class func highlighterYellow() -> UIColor {
        return UIColor(red:0.95, green:0.95, blue:0.08, alpha:1)
    }
    
    class func highlighterBlue() -> UIColor {
        return UIColor(red:0.05, green:0.84, blue:0.99, alpha:1)
    }
    
    class func highlighterOrange() -> UIColor {
        return UIColor(red:1, green:0.4, blue:0, alpha:1)
    }
    
    class func highlighterGreen() -> UIColor {
        return UIColor(red:0.51, green:0.96, blue:0.18, alpha:1)
    }
    
    class func highlighterPink() -> UIColor {
        return UIColor(red:1, green:0, blue:0.6, alpha:1)
    }
}